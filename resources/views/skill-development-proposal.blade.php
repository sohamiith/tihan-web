NMICPS Technology Innovation Hub on Autonomous Navigation Foundation TiHAN is a Section 8 company founded at IIT Hyderabad. Department of Science and Technology (DST)under the National Mission on Interdisciplinary Cyber-Physical Systems (NM-ICPS), Government of India has sanctioned the prestigious Technology Innovation Hub to IIT Hyderabad in the technological vertical of Autonomous Navigation and Data Acquisition Systems (UAVs, ROVs, etc.). The vision of the hub is to become a global destination for next generation smart mobility technologies that utilize reliable and efficient autonomous navigation and data acquisition systems. TiHAN is recognized as a Scientific and Industrial Research Organization (SIRO) by the Department of Scientific and Industrial Research. With a synergetic industry and academic collaborations, the hub aims at realizing the utilization of autonomous navigation and data acquisition systems in the Indian as well as international context. TiHAN in collaboration with IIT Hyderabad has established a New Interdisciplinary 2-year M. Tech program on Smart mobility which is first of its kind in the country.</p>

